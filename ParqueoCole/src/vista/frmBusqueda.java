    private void btnDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDatosActionPerformed

        String est="";
        try {

            Conexion conn = new Conexion();
            Connection con = conn.getConexion();
            PreparedStatement ps = null;
            ResultSet rs = null;

            //String sql1 = "SELECT cedula,espacio,horaEntrada,horaSalida FROM tb_asignar WHERE espacio=?";
            String sql ="SELECT p.nombre, p.apellido1, p.apellido2, p.placa, a.espacio, a.horaEntrada, a.estado FROM tb_asignar AS a INNER JOIN tb_personal AS p ON a.cedula=p.cedula WHERE espacio=?";
            ps = con.prepareStatement(sql);
            ps.setInt(1, i);
            rs = ps.executeQuery();

            while (rs.next()) {
                

                if("Libre".equals(rs.getString("estado"))){
                    JOptionPane.showMessageDialog(null, "Espacio "+i+" Esta Libre");
                }
                else if("Ocupado".equals(rs.getString("estado"))){
                    JOptionPane.showMessageDialog(null, "Estado: "+rs.getObject("estado")+"\n"+"Nombre: "+rs.getObject("nombre")+"\n"+"Apellido1: "+rs.getObject("apellido1")+"\n"+"Apellido2: "+rs.getObject("apellido2")+"\n"+"Placa: "+
                        rs.getObject("placa")+"\n"+"Espacio: "+rs.getObject("espacio")+"\n"+"Hora de Entrada: "+rs.getObject("horaEntrada"));
                }

            }

        } catch (SQLException ex) {
            System.err.println(ex.toString());
        }
    }//GEN-LAST:event_btnDatosActionPerformed

