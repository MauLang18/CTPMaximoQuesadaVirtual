    private void DatosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_DatosKeyReleased

        try {
            Conexion objCon = new Conexion();
            Connection conn = objCon.getConexion();
            PreparedStatement ps = null;
            ResultSet rs = null;

            int Fila = Datos.getSelectedRow();
            String codigo = Datos.getValueAt(Fila, 0).toString();

            ps = conn.prepareStatement("SELECT codi,pre,exis FROM tb_libros WHERE codi=?");
            ps.setString(1, codigo);
            rs = ps.executeQuery();

            while (rs.next()) {
                txtCodigo.setText(rs.getString("codi"));
                txtPrecio.setText(rs.getString("pre"));
                txtExistencias.setText(rs.getString("exis"));
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_DatosKeyReleased

    private void txtPrecioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrecioKeyPressed

        if (evt.getKeyCode() == 40) {
            txtExistencias.requestFocus();
        }

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            li.setLibro(txtLibro.getSelectedItem().toString());
            li.setGrado(txtGrado.getSelectedItem().toString());
            li.setPrecio(Integer.parseInt(txtPrecio.getText()));
            li.setExistencias(Integer.parseInt(txtExistencias.getText()));

            if (liC.ingresar(li)) {
                JOptionPane.showMessageDialog(null, "Ingresado con exito");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al ingresar");
                limpiar();
            }

            btnAgregar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnModificar.setEnabled(false);
        }
    }//GEN-LAST:event_txtPrecioKeyPressed

