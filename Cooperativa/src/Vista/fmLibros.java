    public fmLibros() {
        initComponents();
        setTitle("Libros");

        this.Datos.setModel(m5);
        this.m5.addColumn("Codigo");
        this.m5.addColumn("Materia");
        this.m5.addColumn("Grado");
        this.m5.addColumn("Precio");
        this.m5.addColumn("Existencias");

        try {
            Datos.setModel(m5);

            Conexion conn = new Conexion();
            Connection con = conn.getConexion();
            PreparedStatement ps = null;
            ResultSet rs = null;

            String sql = "SELECT codi,libro,grado,pre,exis FROM tb_libros";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumn = rsMd.getColumnCount();

            while (rs.next()) {

                Object[] filas = new Object[cantidadColumn];

                for (int i = 0; i < cantidadColumn; i++) {
                    filas[i] = rs.getObject(i + 1);
                }

                m5.addRow(filas);
            }

        } catch (SQLException ex) {
            System.err.println(ex.toString());
        }
    }

